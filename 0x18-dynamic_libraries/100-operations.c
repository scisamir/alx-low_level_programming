/**
 * add - adds two numbers, a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of the addition of 'a' and 'b'
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subracts two numbers, a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of the substraction of 'a' and 'b'
 */

int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiplies two numbers, a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of the multiplication of 'a' and 'b'
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divides two numbers, a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of the division of 'a' and 'b'
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - mods two numbers, a and b
 * @a: first number
 * @b: second number
 *
 * Return: result of the mod of 'a' and 'b'
 */

int mod(int a, int b)
{
	return (a % b);
}
